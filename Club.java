//class for club
public class Club {
	private int mean;
	private int std;
	
	public Club(int mean, int std) {
		this.mean=mean;
		this.std=std;
	}
	
	public int getMean() {
		return mean;
	}
	public void setMean(int mean) {
		this.mean = mean;
	}
	public int getStd() {
		return std;
	}
	public void setStd(int std) {
		this.std = std;
	}
	
	
}

